#pragma once

namespace xit::Drawing
{
    /**
     * @brief Defines the different orientations that a control or layout can have.
     */
    enum class Orientation
    {
        /**
         * @brief Control or layout should be horizontally oriented.
         */
        Horizontal = 0,
        /**
         * @brief Control or layout should be vertically oriented.
         */
        Vertical = 1
    };
}
using namespace xit::Drawing;
